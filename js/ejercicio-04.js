// Declarar una variable llamada "temperaturaCelsius" y asignarle un valor numérico que
// represente una temperatura en grados Celsius. Convertir esa temperatura a grados
// Fahrenheit (usando la fórmula (temperaturaCelsius * 9/5) + 32) y mostrar el resultado en la
// consola.

'use strict';

let temperaturaCelsius = 30;
let gradosFahrenheit = (temperaturaCelsius * 9) / 5 + 32;

console.log(`Grados Fahrenheit: ${gradosFahrenheit}°F`);
